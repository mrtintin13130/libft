#include "libft.h"

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	size_t	i;
	char	*s;
	char	*d;

	s = (char *)src;
	d = (char *)dst;
	i = -1;
	while (++i < n)
		d[i] = s[i];
	dst = d;
	return (dst);
}
